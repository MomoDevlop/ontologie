// Fonction de d√©marrage du serveur
async function startServer() {
  try {
    // V√©rification des variables d'environnement
    if (!process.env.NEO4J_URI || !process.env.NEO4J_USERNAME || !process.env.NEO4J_PASSWORD) {
      throw new Error('Variables d\'environnement Neo4j manquantes. V√©rifiez votre fichier .env');
    }

    // Connexion √† Neo4j
    console.log('üîÑ Connexion √† Neo4j en cours...');
    await neo4jConnection.connect();
    
    // V√©rifier si la base de donn√©es est vide et la peupler si n√©cessaire
    try {
      const result = await neo4jConnection.executeQuery('MATCH (n) RETURN count(n) as nodeCount', {}, 'READ');
      const nodeCount = result.records[0].get('nodeCount').toNumber();
      
      if (nodeCount === 0) {
        console.log('üóÑÔ∏è  Base de donn√©es vide d√©tect√©e');
        console.log('üöÄ D√©but de la population automatique...');
        const populator = new DatabasePopulator();
        await populator.populateDatabase();
        console.log('‚úÖ Population automatique termin√©e');
      } else {
        console.log(`üóÑÔ∏è  Base de donn√©es contient d√©j√† ${nodeCount} n≈ìuds`);
      }
    } catch (error) {
      console.warn('‚ö†Ô∏è  Impossible de v√©rifier l\'√©tat de la base de donn√©es:', error.message);
      console.log('üí° Vous pouvez toujours peupler manuellement avec POST /populate-database');
    }
    
    // D√©marrage du serveur
    app.listen(PORT, () => {
      console.log(`üöÄ Serveur d√©marr√© sur le port ${PORT}`);
      console.log(`üìç URL: http://localhost:${PORT}`);
      console.log(`üåç Environnement: ${process.env.NODE_ENV || 'development'}`);
      console.log(`üóÑÔ∏è  Base de donn√©es: ${process.env.NEO4J_URI}`);
      console.log('');
      console.log('üìö Documentation API disponible sur: http://localhost:' + PORT);
      console.log('üîç Test de sant√©: http://localhost:' + PORT + '/health');
      console.log('');
    });

  } catch (error) {
    console.error('‚ùå Erreur lors du d√©marrage du serveur:', error);
    console.error('üí° V√©rifiez que Neo4j est d√©marr√© et que les variables d\'environnement sont correctes');
    process.exit(1);
  }
}

